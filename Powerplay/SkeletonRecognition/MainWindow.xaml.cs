            sensor = KinectSensor.KinectSensors.Where(s => s.Status == KinectStatus.Connected).FirstOrDefault();
            
            
            sensor.SkeletonStream.Enable();
            sensor.SkeletonFrameReady +=sensor_SkeletonFrameReady;
            sensor.Start();

            
        }

