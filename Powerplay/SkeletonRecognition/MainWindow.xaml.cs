                    skeletonData = new Skeleton[e.OpenSkeletonFrame().SkeletonArrayLength];
                    frame.CopySkeletonDataTo(skeletonData);
                    activeSkeleton = skeletonData.Where(sd => sd.TrackingState == SkeletonTrackingState.Tracked).FirstOrDefault();

                    
                    if (activeSkeleton != null)
                    {
                        SkeletonPosition.Text = activeSkeleton.Position.Z.ToString();

                        // Method-1 : Direct Gesture Recognition
                        //SkeletonPoint handRight = activeSkeleton.Joints[JointType.HandRight].Position;
                        //SkeletonPoint elbowRight = activeSkeleton.Joints[JointType.ElbowRight].Position;

                        //if (handRight.Y > elbowRight.Y)
                        //{
                        //    DetectedGestureName.Text = "Hand Up";
                        //}
                        //else
                        //{
                        //    DetectedGestureName.Text = "Hand Down";
                        //}

                        // Method-2 : GestureRecognition through Gesture Engine
                        if (ge.Update(activeSkeleton) == "powerplay")
                        {
                            DetectedGestureName.Text = "Powerplay Detected";
                            this.SkeletalImage.Source = new BitmapImage(new Uri(@"Images/namaste.jpg", UriKind.RelativeOrAbsolute));
                        }
                        else
                        {
                            DetectedGestureName.Text = "In progress";
                            this.SkeletalImage.Source = new BitmapImage(new Uri(@"Images/none.jpg", UriKind.RelativeOrAbsolute));
                        }

                    }

                }
            }

            // Drawing
            using (DrawingContext dc = this.drawingGroup.Open())
            {
                // Draw a transparent background to set the render size
                dc.DrawRectangle(Brushes.Black, null, new Rect(0.0, 0.0, RenderWidth, RenderHeight));

                if (skeletonData.Length != 0)
                {
                    foreach (Skeleton skel in skeletonData)
                    {
                        RenderClippedEdges(skel, dc);

                        if (skel.TrackingState == SkeletonTrackingState.Tracked)
                        {
                            this.DrawBonesAndJoints(skel, dc);
                        }
                        else if (skel.TrackingState == SkeletonTrackingState.PositionOnly)
                        {
                            dc.DrawEllipse(
                            this.centerPointBrush,
                            null,
                            this.SkeletonPointToScreen(skel.Position),
                            BodyCenterThickness,
                            BodyCenterThickness);
                        }
                    }
                }

                // prevent drawing outside of our render area
                this.drawingGroup.ClipGeometry = new RectangleGeometry(new Rect(0.0, 0.0, RenderWidth, RenderHeight));
            }
            
